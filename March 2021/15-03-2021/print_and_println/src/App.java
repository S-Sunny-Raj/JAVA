public class App {
    public static void main(String[] args) throws Exception {
        // println() method used to print on the screen and ln here moves to the next
        // line
        System.out.println("Sunny Raj");
        System.out.println("24"); // this number is also a string
        System.out.println(""); // blank space
        System.out.println("Raj Sunny");
        // print() method used to print everything in the same line
        System.out.print("Sunny");
        System.out.print(" ");
        System.out.print("Raj");
        System.out.print("123");
    }
}
